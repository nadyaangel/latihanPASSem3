class Mahasiswa{
    private int NIM;
    private String Nama;
    private String jurusan;
    private float IPK;

    //konstruktor default
    public Mahasiswa(){
        NIM = 0;
        Nama = Nama;
        jurusan = jurusan;
        IPK = 0;
    }

    //konstruktor dengan parameter
    public Mahasiswa(int newNIM, String newNama, String newJurusan, float newIPK){
        NIM = newNIM;
        Nama = newNama;
        jurusan = newJurusan;
        IPK = newIPK;
    }
}
